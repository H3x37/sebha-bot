export default {
  data: new SlashCommandBuilder()
    .setName('صحابي')
    .setDescription('نبذة عن أحد صحابة رسول الله صلى الله عليه وسلم')
    .addStringOption(option =>
      option.setName('الاسم')
        .setDescription('أدخل اسم الصحابي (كامل أو جزء منه)')
        .setRequired(true)
        .setAutocomplete(false)),

  async execute(interaction: ChatInputCommandInteraction) {
    let query = interaction.options.getString('الاسم', true).trim();
    const normalizedQuery = normalizeArabic(query);

    const results = sahabaDB.filter(s =>
      s.name.includes(query) ||
      s.title.includes(query) ||
      normalizeArabic(s.name).includes(normalizedQuery) ||
      normalizeArabic(s.title).includes(normalizedQuery)
    );

    if (results.length === 0) {
      await interaction.reply({
        embeds: [errorEmbed(`لم نجد صحابياً بهذا الاسم. تأكد من الاسم أو ابحث باسم آخر.`)],
      });
      return;
    }

    const sahabi = results[0];

    const embed = buildEmbed('history', {
      title: `${sahabi.name} رضي الله عنه`,
      fields: [
        { name: 'اللقب', value: sahabi.title, inline: false },
        { name: 'ولادته', value: sahabi.birth, inline: true },
        { name: 'وفاته', value: sahabi.death, inline: true },
        { name: 'سيرته', value: sahabi.biography, inline: false },
        { name: 'فضائله', value: sahabi.virtues, inline: false },
      ],
    });

    await interaction.reply({ embeds: [embed] });
  },
  category: 'history',
} as Command;
