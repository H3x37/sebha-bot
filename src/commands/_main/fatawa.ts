export default {
  data: new SlashCommandBuilder()
    .setName('فتوى')
    .setDescription('الأمر الموحد للفتاوى والمسائل الدينية')
    .addSubcommand(sub =>
      sub.setName('اليوم')
        .setDescription('فتوى عشوائية من الفقه الإسلامي'))
    .addSubcommand(sub =>
      sub.setName('بحث')
        .setDescription('البحث عن فتوى في موضوع معين')
        .addStringOption(option =>
          option.setName('موضوع')
            .setDescription('الكلمة المفتاحية للبحث')
            .setRequired(true)))
    .addSubcommand(sub =>
      sub.setName('سؤال')
        .setDescription('سؤال ديني عشوائي مع إمكانية إظهار الإجابة'))
    .addSubcommand(sub =>
      sub.setName('مصطلح')
        .setDescription('تعريف مصطلح شرعي')
        .addStringOption(option =>
          option.setName('الكلمة')
            .setDescription('المصطلح المراد تعريفه')
            .setRequired(true))),

  async execute(interaction: ChatInputCommandInteraction) {
    const sub = interaction.options.getSubcommand();

    if (sub === 'اليوم') {
      const random = fatwaDB[Math.floor(Math.random() * fatwaDB.length)];

      const embed = buildEmbed('fatawa', {
        author: 'فتاوى إسلامية',
        title: '⚖️ فتوى اليوم',
        fields: [
          { name: bold('السؤال'), value: blockquote(random.question) },
          { name: bold('الجواب'), value: blockquote(random.answer) },
          { name: bold('المصدر'), value: random.source },
        ],
      });

      await interaction.reply({ embeds: [embed] });
    } else if (sub === 'بحث') {
      const keyword = interaction.options.getString('موضوع', true);

      const results = fatwaDB.filter(e =>
        e.question.includes(keyword) || e.answer.includes(keyword),
      );

      if (results.length === 0) {
        await interaction.reply({ embeds: [errorEmbed(`لم يتم العثور على فتوى تتعلق بـ "${keyword}".`)], flags: 64 });
        return;
      }

      const embed = buildEmbed('fatawa', {
        author: 'فتاوى إسلامية',
        title: `⚖️ ${keyword}`,
        description: blockquote(results.slice(0, 1).map(e => e.answer).join('\n\n')),
        fields: results.slice(0, 3).map(e => ({
          name: bold(e.question),
          value: e.answer.length > 100 ? e.answer.slice(0, 100) + '...' : e.answer,
        })),
      });

      await interaction.reply({ embeds: [embed] });
    } else if (sub === 'سؤال') {
      const entry = quizDB[Math.floor(Math.random() * quizDB.length)];

      const reveal = new ButtonBuilder()
        .setCustomId('reveal_answer')
        .setLabel('إظهار الإجابة')
        .setStyle(ButtonStyle.Primary);

      const row = new ActionRowBuilder<ButtonBuilder>().addComponents(reveal);

      const embed = buildEmbed('fatawa', {
        author: 'أسئلة دينية',
        title: '⚖️ سؤال',
        description: blockquote(entry.question + '\n\nاضغط الزر أدناه لإظهار الإجابة'),
      });

      const reply = await interaction.reply({
        embeds: [embed],
        components: [row],
      });

      const collector = reply.createMessageComponentCollector({
        componentType: ComponentType.Button,
        time: 60_000,
      });

      collector.on('collect', async (btnInteraction) => {
        if (btnInteraction.user.id !== interaction.user.id) {
          await btnInteraction.reply({ content: 'هذا الزر ليس لك.', flags: 64 });
          return;
        }

        const answerEmbed = buildEmbed('fatawa', {
          author: 'أسئلة دينية',
          title: '⚖️ الإجابة',
          fields: [
            { name: bold('السؤال'), value: blockquote(entry.question) },
            { name: bold('الإجابة'), value: blockquote(entry.answer) },
          ],
        });

        const disabledBtn = ButtonBuilder.from(reveal).setDisabled(true);
        const disabledRow = new ActionRowBuilder<ButtonBuilder>().addComponents(disabledBtn);

        await btnInteraction.update({ embeds: [answerEmbed], components: [disabledRow] });
        collector.stop();
      });

      collector.on('end', async (_collected, reason) => {
        if (reason === 'time') {
          const expiredBtn = ButtonBuilder.from(reveal).setDisabled(true).setLabel('انتهى الوقت');
          const expiredRow = new ActionRowBuilder<ButtonBuilder>().addComponents(expiredBtn);
          await interaction.editReply({ components: [expiredRow] }).catch(() => {});
        }
      });
    } else if (sub === 'مصطلح') {
      const keyword = interaction.options.getString('الكلمة', true);

      const entry = glossary.find(e => e.term === keyword);

      if (!entry) {
        const suggestions = glossary.filter(e => e.term.includes(keyword));
        if (suggestions.length > 0) {
          const list = suggestions.map(e => `• ${e.term}`).join('\n');
          await interaction.reply({
            embeds: [buildEmbed('fatawa', {
              author: 'موسوعة المصطلحات',
              title: '⚖️ مصطلحات مشابهة',
              description: blockquote(`لم أجد "${keyword}" بالضبط.\nهل تقصد أحد هذه المصطلحات؟\n\n${list}`),
            })],
            flags: 64,
          });
          return;
        }
        await interaction.reply({ embeds: [errorEmbed(`لم يتم العثور على مصطلح "${keyword}".`)], flags: 64 });
        return;
      }

      const embed = buildEmbed('fatawa', {
        author: 'موسوعة المصطلحات',
        title: `⚖️ ${entry.term}`,
        description: blockquote(entry.definition),
      });

      await interaction.reply({ embeds: [embed] });
    }
  },
  category: 'fatawa',
} as Command;
